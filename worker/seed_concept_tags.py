def generate_sql():
    lines = []
    for concept, symbols in CONCEPT_MAP.items():
        for sym in symbols:
            name = concept.replace("'", "''")
            lines.append(f"('{sym}', '{name}', 'seed', 1.0)")

    # 分批，每批 100
    batch_size = 100
    all_sql = []
    for i in range(0, len(lines), batch_size):
        batch = lines[i:i+batch_size]
        sql = "INSERT OR IGNORE INTO stock_tags (symbol, tag, source, weight) VALUES\n"
        sql += ",\n".join(batch) + ";\n"
        all_sql.append(sql)

    return "\n".join(all_sql)

if __name__ == "__main__":
    sql = generate_sql()
    # 寫檔
    with open("seed_tags.sql", "w", encoding="utf-8") as f:
        f.write(sql)
    # 統計
    total_tags = sum(len(v) for v in CONCEPT_MAP.values())
    print(f"Generated {total_tags} tags across {len(CONCEPT_MAP)} concepts")
    print(f"Written to seed_tags.sql")
